def _counter_key() -> str:
    return "c2:counter"


async def _next_id(prefix: str) -> str:
    """Atomic counter for unique IDs."""
    val = await c2_server.redis.incr(f"{_counter_key()}:{prefix}")
    return f"{prefix}-{val:06d}"


