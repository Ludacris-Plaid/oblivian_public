async def _cleanup_mock_data():
    """Remove ALL mock data generated by the simulation from Redis — no trace left behind."""
    mock_node_ids = [n["node_id"] for n in MOCK_NODES]
    keys_to_delete = []
    for nid in mock_node_ids:
        keys_to_delete.append(f"c2:node:{nid}")
        keys_to_delete.append(f"c2:evasion:{nid}")
        keys_to_delete.append(f"c2:pending_cmds:{nid}")

    # Also wipe shared simulation artifacts
    keys_to_delete.extend([
        "c2:credentials",
        "c2:evasion:latest",
        "c2:counter:cred",
        "c2:events",              # event log: all simulated heartbeat/cred/evasion events
        "c2:mutation:active",     # mutation config pushed by ingest_beacon_threats
        "c2:bytes_harvested",     # bytes counter (keep clean when sim is off)
    ])

    if keys_to_delete:
        await c2_server.redis.delete(*keys_to_delete)

    # Also scan and delete all c2:cred:full:* keys (individual full-credential records)
    # These have 30-min TTL but should be purged immediately on sim stop
    try:
        cursor = 0
        while True:
            cursor, cred_keys = await c2_server.redis.scan(cursor, match="c2:cred:full:*", count=100)
            if cred_keys:
                await c2_server.redis.delete(*cred_keys)
            if cursor == 0:
                break
    except Exception:
        pass  # Redis scan may fail on some providers, the TTL cleanup handles it

    logger.info(f"[SIM] Cleaned up ALL mock data ({len(keys_to_delete)} direct keys + full-cred records)")
    await c2_server.redis.delete("c2:events")  # delete the list and re-init empty
    await c2_server._push_event({
        "timestamp": time.strftime("%Y-%m-%dT%H:%M:%SZ", time.gmtime()),
        "type": "system",
        "payload": {"action": "simulation_disabled", "detail": "All mock data purged"}
    })


